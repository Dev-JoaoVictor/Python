num1 = float(input('Digite o 1° número:'))
num2 = float(input('Digite o 2° número:'))

print(f'A soma {num1} + {num2} = { num1 + num2 }')
print(f'{num1} - {num2} = { num1 - num2 }')
print(f'{num1} * {num2} = { num1 * num2 }')
print(f'{num1} / {num2} = {(num1 / num2):.2f}')
print(f'{num1} % {num2} = { num1 % num2 }')