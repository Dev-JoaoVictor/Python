print('Sejá bem vindo a calculadora!')
num1 = int(input('Digite o primeiro número: '))
num2 = int(input('Digite o segundo número: '))

soma = num1 + num2
subtracao = num1 - num2
divisao = num1 / num2
multiplicacao = num1 * num2 
resto = num1 % num2

print(f' A soma dos números {num1} e {num2} é: {soma}')
print(f' A subtracao dos números {num1} e {num2} é: {subtracao}')
print(f' A divisao dos números {num1} e {num2} é: {divisao}')
print(f' A multiplicacao dos números {num1} e {num2} é: {multiplicacao}')
print(f' A resto dos números {num1} e {num2} é: {resto}')